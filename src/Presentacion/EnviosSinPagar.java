    public void ActualizarLista(){
        DefaultTableModel model = (DefaultTableModel) tableEnvio.getModel();
        int i = tableEnvio.getRowCount();
        while (i != 0) {
            model.removeRow(0);
            i--;
        }
        this.cargarLosEnviosSinPagar();
    }
    
