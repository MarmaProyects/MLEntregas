    private void cargarLosEnviosSinPagar() {
        ArrayList<Envio> listaDeEnvios = this.IE.listaDeEnvios();
        String estado;
        String fecha;
        DefaultTableModel modelo = (DefaultTableModel) this.tableEnvio.getModel();
        for (Envio envio : listaDeEnvios) {
            if (envio.getPago().getPago() == null) {
                estado = envio.getEstados().get(envio.getEstados().size() - 1).getTipo().getEstado();
                fecha = envio.getEstados().get(0).getFecha().toString();
                Object[] row = {envio.getIdEnvio(), envio.getPaquete().getDescripcion(), envio.getClienteEmisor().getNombre() + " " + envio.getClienteEmisor().getApellido(), envio.getPago().getPrecio(), estado, fecha, envio.getPago().getIdPago()};
                modelo.addRow(row);
            }

        }

        this.tableEnvio.getColumnModel().getColumn(0).setCellRenderer(new CenterRenderer());
        this.tableEnvio.getColumnModel().getColumn(1).setCellRenderer(new CenterRenderer());
        this.tableEnvio.getColumnModel().getColumn(2).setCellRenderer(new CenterRenderer());
        this.tableEnvio.getColumnModel().getColumn(3).setCellRenderer(new CenterRenderer());
        this.tableEnvio.getColumnModel().getColumn(4).setCellRenderer(new CenterRenderer());
    }

