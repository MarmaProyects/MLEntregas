    private void altaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altaButtonActionPerformed
        // TODO add your handling code here:
        String metodoPago = this.metodoPagoComboBox.getSelectedItem().toString();
        float pago = Float.parseFloat(this.pagoField.getText().trim());
        if (metodoPago.equals(" ") || this.pagoField.getText().trim().equals(" ")) {
            JOptionPane.showMessageDialog(null, "El método de pago o el monto no han sido ingresados", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.IE.facturarEnvio(pago, metodoPago);
        JOptionPane.showMessageDialog(null, "Pago ejecutado exitosamente!", "Pago exitoso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_altaButtonActionPerformed

